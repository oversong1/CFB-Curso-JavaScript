/*Vamos passar parametros dentro da função
ou seja vamos passar regras que vai
fazer ela funcionar de acordo com o
que se propoem */

function soma(n1 , n2){
    let res ;
    res = n1 + n2;
    return res;
}

/*Veja dentro dos colchetes os parametros
  estão sendo passado 
*/
console.log(soma(10, 3));

let resultado_soma = soma(7, 8);
console.log("resultado " + resultado_soma);